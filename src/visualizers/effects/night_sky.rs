/// Map B-V color index to an ANSI 256-color code approximating stellar spectral color.
fn bv_to_ansi(bv: f32) -> u8 {
    const STOPS: &[(f32, u8)] = &[
        (-0.40, 195), // O/B hot blue
        (-0.10, 189), // blue-white
        ( 0.00, 231), // white A-type
        ( 0.20, 230), // warm white
        ( 0.45, 229), // cream F-type
        ( 0.65, 226), // pale yellow G-type
        ( 0.85, 220), // yellow
        ( 1.20, 214), // orange K-type
        ( 1.60, 208), // deep orange
        ( 2.00, 196), // red M-type
    ];
    for i in 0..STOPS.len() - 1 {
        if bv <= STOPS[i + 1].0 {
            // snap to nearer stop
            return if (bv - STOPS[i].0).abs() < (bv - STOPS[i + 1].0).abs() {
                STOPS[i].1
            } else {
                STOPS[i + 1].1
            };
        }
    }
    STOPS.last().unwrap().1
}

/// Map B-V + color_mode string to the ANSI color to render with.
fn star_color(base_color: u8, shimmer: f32, mode: &str) -> u8 {
    match mode {
        "mono" => if shimmer > 0.5 { 231 } else { 255 },
        "teal" => {
            // blue-green tones based on magnitude/shimmer
            if shimmer > 0.6 { 87 } else if shimmer > 0.3 { 51 } else { 45 }
        }
        "warm" => {
            if shimmer > 0.6 { 231 } else if shimmer > 0.3 { 229 } else { 226 }
        }
        _ => base_color, // "spectral"
    }
}

/// Map galactic position + density to an ANSI 256 color for Milky Way rendering.
/// Returns (ansi_color, use_normal_brightness) — false means caller applies \x1b[2m (dim).
fn mw_color(l_deg: f32, b_deg: f32, density: f32) -> (u8, bool) {
    let near_center = l_deg < 30.0 || l_deg > 330.0;
    let cygnus_arm  = l_deg > 65.0 && l_deg < 95.0;
    let sag_arm     = l_deg > 15.0 && l_deg < 55.0;
    let anticenter  = l_deg > 170.0 && l_deg < 190.0;
    let b_abs       = b_deg.abs();

    if near_center && b_abs < 12.0 {
        // Galactic centre bulge: warm amber/orange — dust and red giant stars
        if density > 1.5      { (208, true)  }
        else if density > 0.8 { (214, false) }
        else if density > 0.5 { (220, false) }
        else                  { (229, false) }
    } else if cygnus_arm && b_abs < 8.0 {
        // Cygnus arm: blue-white — young OB stars and ionised gas
        if density > 0.7      { (159, false) }
        else if density > 0.4 { (153, false) }
        else                  { (189, false) }
    } else if sag_arm && b_abs < 8.0 {
        // Sagittarius arm: warm white to cream
        if density > 0.7 { (230, false) } else { (229, false) }
    } else if anticenter {
        // Anticentre: faint cool blue-white
        if density > 0.4 { (189, false) } else { (195, false) }
    } else {
        // General disk: near-white
        if density > 0.8      { (255, false) }
        else if density > 0.5 { (253, false) }
        else if density > 0.3 { (250, false) }
        else                  { (247, false) }
    }
}

/// Map visual magnitude + shimmer to a display character via a promotion table.
///
/// Character ladder: · (dim)  +  *  ✦ (bold on peak shimmer)
/// Each star has a base level from its magnitude; shimmer independently boosts
/// that level, so even faint stars (mag 4.5) visibly change on a beat.
fn mag_to_char(mag: f32, shimmer: f32) -> (char, bool) {
    const CHARS: &[char] = &['\u{00b7}', '+', '*', '\u{2726}'];  // ·  +  *  ✦
    let base_level = if mag <= 0.5 { 3 }
                     else if mag <= 1.5 { 2 }
                     else if mag <= 2.5 { 1 }
                     else { 0 };
    let boost = if shimmer > 0.70 { 3 }
                else if shimmer > 0.45 { 2 }
                else if shimmer > 0.20 { 1 }
                else { 0 };
    let level = (base_level + boost).min(3);
    let bold  = shimmer > 0.55 || mag <= 0.5;
    (CHARS[level], bold)
}

